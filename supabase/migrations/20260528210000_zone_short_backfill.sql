-- Adds bosses_local.zone_short and backfills both bosses_local + encounters.
-- Source data: data/bosses.json (113 npc_id → zone long_name pairs).
-- Resolution: long_name → eqemu_zone.short_name (canonical).

alter table bosses_local
  add column if not exists zone_short text references eqemu_zone(short_name) on delete set null;

create index if not exists bosses_local_zone_idx on bosses_local (zone_short);

